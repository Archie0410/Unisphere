export const getUniversityById = (id: number): University | undefined => {
  return universityDatabase.find(uni => uni.id === id);
};

export const searchUniversities = (query: string): University[] => {
  const lowercaseQuery = query.toLowerCase();
  return universityDatabase.filter(uni => 
    uni.name.toLowerCase().includes(lowercaseQuery) ||
    uni.location.toLowerCase().includes(lowercaseQuery) ||
    uni.state.toLowerCase().includes(lowercaseQuery) ||
    uni.city.toLowerCase().includes(lowercaseQuery) ||
    uni.programs.some(program => program.toLowerCase().includes(lowercaseQuery)) ||
    uni.specializations.some(spec => spec.toLowerCase().includes(lowercaseQuery)) ||
    uni.entranceExams.some(exam => exam.toLowerCase().includes(lowercaseQuery))
  );
};

// Helper function to get universities by marks range
export const getUniversitiesByMarks = (marks: number): University[] => {
  if (marks >= 90) {
    return universityDatabase.filter(uni => uni.nirf_rank <= 10);
  } else if (marks >= 80) {
    return universityDatabase.filter(uni => uni.nirf_rank <= 50);
  } else if (marks >= 70) {
    return universityDatabase.filter(uni => uni.nirf_rank <= 100);
  } else if (marks >= 60) {
    return universityDatabase.filter(uni => uni.nirf_rank <= 150);
  } else {
    return universityDatabase.filter(uni => uni.nirf_rank > 150 || uni.acceptance.includes('%') && parseInt(uni.acceptance) >= 50);
  }
};

// Helper function to get universities by subject/stream
export const getUniversitiesBySubject = (subject: string): University[] => {
  const subjectMap: { [key: string]: string[] } = {
    'science': ['engineering', 'technology', 'science', 'medicine'],
    'commerce': ['business', 'commerce', 'management', 'finance'],
    'arts': ['humanities', 'arts', 'liberal-arts', 'social-sciences', 'design'],
    'engineering': ['engineering', 'technology'],
    'medicine': ['medicine', 'healthcare'],
    'law': ['law', 'legal-studies'],
    'design': ['design', 'arts', 'creative'],
    'management': ['management', 'business', 'finance']
  };
  
  const targetSpecializations = subjectMap[subject.toLowerCase()] || [subject.toLowerCase()];
  
  return universityDatabase.filter(uni => 
    uni.specializations.some(spec => 
      targetSpecializations.some(target => spec.includes(target) || target.includes(spec))
    )
  );
};
